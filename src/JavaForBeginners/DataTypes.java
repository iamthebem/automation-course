package JavaForBeginners;

public class DataTypes {
    public static void main(String[] args) {
        // Numerical Primitive
        int integer = 1;
        Integer integerRef = 1;
        short shortNumber = 32000;
        Short shortNumberRef = 32000;
        long longNumber = 1234567L; // Number is always accompanied by 'L'
        Long longNumberRef = 1234567L;
        double doubleNUmber = 3.14;
        Double doubleNumberRef = 3.14;
        float floatNumber = 52.3f; // Number is always accompanied by 'f'
        Float floatNumberRef = 52.3f;

        // Non-Numerical Primitives
        char character = 'a';
        Character characterRef = 'a';
        boolean booleanValue = true;
        Boolean booleanValueRef = false;
        byte myByte = 120;
        Byte myByteRef = 120;

        System.out.println(integer);
        System.out.println(longNumber);
    }
}
