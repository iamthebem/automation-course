package JavaForBeginners;

public class IfStatements {
    public static void main(String[] args) {
        int Integer = 10;

        if (Integer > 5) {
            System.out.println("If statement is true");
        }
        else if(Integer < 10) {
            System.out.println("Else If statement is printed");
        }
        else {
            System.out.println("Else statement is printed");
        }
    }
}
